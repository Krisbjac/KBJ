html.onmouseover = function() { 
    html.style.height = '200px'; 
    html.style.opacity = '40%';
    html.style.marginBottom = '50px'}

html.onmouseout = function() { 
        html.style.height = '150px';
        html.style.opacity = '100%';
        html.style.marginBottom = '100px'};

